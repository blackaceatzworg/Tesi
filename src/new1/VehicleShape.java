package new1;

public class VehicleShape {
	private int lenght;
	private int width;
	
	public VehicleShape(int l,int w){
		this.setLenght(l);
		this.setWidth(w);
	}

	
	public int getLenght() {
		return lenght;
	}
	public void setLenght(int lenght) {
		this.lenght = lenght;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}
	
	

}
